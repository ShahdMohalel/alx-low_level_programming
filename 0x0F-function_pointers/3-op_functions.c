#include <stdlib.h>
/**
 * op_add - add numbers.
 * @a: number.
 * @b: number.
 * Return: (a + b)
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - subtract numbers
 * @a: number.
 * @b: number.
 * Return: (a - b)
*/
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - multiply numbers
 * @a: number.
 * @b: number.
 * Return: (a * b)
*/
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - divide numbers
 * @a: number.
 * @b: number.
 * Return: (a / b)
*/
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - mod numbers
 * @a: number.
 * @b: number.
 * Return: (a % b)
*/
int op_mod(int a, int b)
{
return (a % b);
}
